static enemy_t create_enemy(char *name, int hp, defense_t def, int gold_reward)
{
    enemy_t = enemy;
    strcpy(enemy.base.name = name);
    enemy.base.hp = hp;
    enemy.base.defense = def;
    enemy.gold_reward = gold_reward;

    return enemy;


}

